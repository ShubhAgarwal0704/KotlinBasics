fun variables()
{
    // Variables
    var name = "John" // mutable "var"    // String
    val age = 25 // immutable "val"      // Int
    var isAlive = true // Boolean
    var marks = 97.4 // Double/ Float
    println("Name: $name")
    println("Age: $age")
    println("isAlive: $isAlive")
    println("Marks: $marks")
}

